package com.chapter7.assignment7;

public class Vehicle {
	private int make;
	private String model;
	public Vehicle(int make, String model) {
		super();
		this.make = make;
		this.model = model;
	}
	public int getMake() {
		return make;
	}
	public void setMake(int make) {
		this.make = make;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	
	
}
